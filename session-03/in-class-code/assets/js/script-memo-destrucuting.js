// 配列のDestructuringとオブジェクトのDestructuringの違い

// > ✅ 配列は「順番」で取り出す
// > ✅ オブジェクトは「名前」で取り出す

// ここが一番の違い。

// ---

// # ① 配列の destructuring

// 配列は **順番（index）** がすべて。

// ```js
// const numbers = [10, 20, 30];

// const [first, second] = numbers;
// ```

// これは内部的にこういう意味：

// ```js
// const first = numbers[0];
// const second = numbers[1];
// ```

// 📌 ポイント

// * 変数名は自由
// * 重要なのは「位置」

// 例えば：

// ```js
// const [a, b] = numbers;
// ```

// でもOK。
// 名前は関係ない。順番が命。

// ---

// # ② オブジェクトの destructuring

// オブジェクトは **プロパティ名** がすべて。

// ```js
// const user = { name: "Lily", age: 25 };

// const { name, age } = user;
// ```

// これはこういう意味：

// ```js
// const name = user.name;
// const age = user.age;
// ```

// 📌 ポイント

// * 順番は関係ない
// * プロパティ名が一致している必要がある

// 順番を変えてもOK：

// ```js
// const { age, name } = user;
// ```

// 問題なし。

// ---

// # 🔥 最大の違いまとめ

// |       | 配列    | オブジェクト |
// | ----- | ----- | ------ |
// | 基準    | 順番    | 名前     |
// | 書き方   | []    | {}     |
// | 重要なのは | index | key    |

// ---

// # 🧠 イメージで覚えると

// 配列：

// > 引き出しが横に並んでる
// > 「左から何番目？」

// オブジェクト：

// > ラベル付きの箱
// > 「nameって書いてある箱ちょうだい」

// ---

// # 🎯 よくある混乱ポイント

// これ間違い：

// ```js
// const { 0 } = numbers; // ❌
// ```

// これはオブジェクトの書き方。

// 配列は必ず `[]`。

// ---

// # 🌱 さらに一歩

// 配列はこうもできる：

// ```js
// const [first, , third] = numbers;
// ```

// 👉 真ん中をスキップできる

// オブジェクトは：

// ```js
// const { name: userName } = user;
// ```

// 👉 別名もつけられる
